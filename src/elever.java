public class elever {
    String namn;
    int personnummer;
    String email;
    int mobilnummer;
    String klass;
    String program;

    public elever(String namn, int personnummer, String email, int mobilnummer, String klass, String program)
    {
        this.namn = namn;
        this.personnummer = personnummer;
        this.email = email;
        this.mobilnummer = mobilnummer;
        this.klass = klass;
        this.program = program;
    }
}
